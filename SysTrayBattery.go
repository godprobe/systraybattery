package main

import (
	"fmt"
	"time"

	"github.com/distatus/battery"
	"github.com/getlantern/systray"
)

func main() {
	// Initialize systray
	systray.Run(onReady, onExit)
}

func onReady() {
	// Add battery icon to system tray
	systray.SetIcon(iconData)

	// Add "Quit" item to to system tray menu
	battery := getBattery()
	mExit := systray.AddMenuItem(fmt.Sprintf("(%d%%) Quit", battery), "Exit the application")

	// Refresh battery percentage every 10 seconds
	go func() {
		for {
			battery := getBattery()
			systray.SetTooltip(fmt.Sprintf("Battery: %d%%", battery))
			mExit.SetTitle(fmt.Sprintf("(%d%%) Quit", battery))
			<-time.After(10 * time.Second)
		}
	}()

	// Handle clicks on the exit menu item
	for range mExit.ClickedCh {
		systray.Quit()
		return
	}
}

func onExit() {
	// Clean up systray
	systray.Quit()
}

func getBattery() int {
	// Get battery percentage
	bat, err := battery.Get(0)
	if err != nil {
		return -1
	}
	return int((float64(bat.Current) / float64(bat.Full)) * 100)
}

